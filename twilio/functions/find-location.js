const findLocation = (inputCity) => {
  for (const state in India) {
    inputCity = inputCity.toLowerCase().trim();

    if (state.toLowerCase().trim() === inputCity) {
      return state;
    }

    for (const city of India[state]) {
      if (city.toLowerCase().trim() === inputCity) {
        return state;
      }
    }
  }
  return "404";
};

const checkLocation = (city) => {
  for (const state in India) {
    if (India[state].includes(city)) {
      return true;
    }
  }
  return false;
};

